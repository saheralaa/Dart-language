void main() {
  //! + add
  int a = 5;
  int b = 3;
  int y = a + b;
  print(y);
  //! - subtract
  int s = a - b;
  print(s);
  //! * Multiply
  int m = a * b;
  print(m);
  //! / Divide
  double d = a / b;
  print(d);
  //! ~/ divide / returned intger result
  int dr = a ~/ b;
  print(dr);
  //! % module
  int mo = a % b;
  print(mo);
  int increment = 12;
  increment++;
  print(increment);
  int decrement = 12;
  decrement--;
  print(decrement);
}
