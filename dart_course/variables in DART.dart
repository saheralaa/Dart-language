void main(List<String> args) {
  const int someINTEGER = 10; //integer
  print(someINTEGER);
  const double someDOUBLE = 10.5; //double
  print(someDOUBLE);
  const String someSTRING = 'HELLO'; //string type
  print(someSTRING);
  const bool someBOOLEAN = true; //true or false
  print(!someBOOLEAN);
  const List<int> someLIST = [1, 2, 3]; //list of integer
  print(someLIST);
  const Map<String, String> someMAP = {'FOO': 'bar'}; //MAP of string
  print(someMAP['foo']);
  const Set<int> someSET = {1, 2, 3}; // no loop for numbers in the set
  print(someSET);
  const dynamic someDYNAMIC = null; //dynamic
  print(someDYNAMIC);
  const Symbol someSYMBOL = #name; //statik
  print(someSYMBOL);


}
